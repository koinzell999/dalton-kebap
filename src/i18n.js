// Returns the value for `field` in the requested language, falling back to Turkish.
export function localize(obj, field, lang) {
  if (obj && lang && lang !== 'tr') {
    const val = obj[`${field}_${lang}`];
    if (val && val.trim()) return val;
  }
  return (obj && obj[field]) || '';
}

// Extracts a numeric value from a stored price string (handles "250" and "250 ₺").
export function parsePriceNum(price) {
  if (!price) return 0;
  return parseFloat(String(price).replace(/[^\d.]/g, '')) || 0;
}

// Strips any trailing ₺ from a stored price string and re-appends it at display time.
// Handles both legacy "400 ₺" and clean "400" stored values.
export function formatPrice(price) {
  if (!price) return '';
  const stripped = String(price).trim().replace(/\s*₺\s*$/, '');
  return stripped ? `${stripped} ₺` : '';
}

function detectBrowserLang() {
  try {
    const raw = navigator.language || (navigator.languages && navigator.languages[0]) || 'tr';
    const lang = raw.toLowerCase();
    if (lang.startsWith('ar')) return 'ar';
    if (lang.startsWith('en')) return 'en';
    return 'tr';
  } catch {
    return 'tr';
  }
}

export function useLanguage(mode) {
  const [lang, setLangState] = useState(() => {
    if (mode === 'manual') {
      try { return localStorage.getItem('adminLang') || 'tr'; } catch { return 'tr'; }
    }
    // auto: honour an explicit user choice, fall back to browser detection
    try { const saved = localStorage.getItem('dk:lang'); if (saved) return saved; } catch {}
    return detectBrowserLang();
  });

  function setLang(code) {
    setLangState(code);
    if (mode === 'manual') {
      try { localStorage.setItem('adminLang', code); } catch {}
    } else {
      try { localStorage.setItem('dk:lang', code); } catch {}
    }
  }

  function t(key) {
    return (translations[lang] && translations[lang][key]) ||
           (translations.tr && translations.tr[key]) ||
           key;
  }

  const isRTL = lang === 'ar';

  return { lang, setLang, t, isRTL };
}
